package model;

/**
 *
 * @author acer
 */
public class Category {
    private int id;
    private String createdAt;
    private String name;
    private String slug;

    public Category(int id, String createdAt, String name, String slug) {
        this.id = id;
        this.createdAt = createdAt;
        this.name = name;
        this.slug = slug;
    }

    public Category() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }
    
    
}
